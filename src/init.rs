    #[test]
    fn writes_document_and_requires_force_for_overwrite() {
        let path = unique_temp_path("devkit-init");
        let document = super::InitDocument {
            content: "[policy]\nchannel = \"stable\"\n".to_string(),
        };

        let first = write_init_document(&path, false, &document).unwrap();
        assert!(!first.overwritten);
        assert_eq!(fs::read_to_string(&path).unwrap(), document.content);

        let error = write_init_document(&path, false, &document).unwrap_err();
        assert!(error.to_string().contains("already exists"));

        let second = write_init_document(&path, true, &document).unwrap();
        assert!(second.overwritten);

        let _ = fs::remove_file(path);
    }

