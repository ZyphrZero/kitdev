fn installed_tool<'a>(report: &'a DoctorReport, name: &str) -> Option<&'a ToolStatus> {
    report
        .tools
        .iter()
        .find(|tool| tool.name == name && !matches!(tool.status, Status::Missing))
}

fn detect_installed_commands(commands: &[&str]) -> Vec<String> {
    commands
        .iter()
        .filter(|command| command_path(command).is_some())
        .map(|command| (*command).to_string())
        .collect()
}

fn quoted_list(items: &[String]) -> String {
    items
        .iter()
        .map(|item| toml_string(item))
        .collect::<Vec<_>>()
        .join(", ")
}

fn toml_string(value: &str) -> String {
    let mut escaped = String::with_capacity(value.len() + 2);
    escaped.push('"');
    for character in value.chars() {
        match character {
            '\\' => escaped.push_str("\\\\"),
            '"' => escaped.push_str("\\\""),
            '\n' => escaped.push_str("\\n"),
            '\r' => escaped.push_str("\\r"),
            '\t' => escaped.push_str("\\t"),
            character if character.is_control() => {
                escaped.push_str(&format!("\\u{:04X}", character as u32));
            }
            character => escaped.push(character),
        }
    }
    escaped.push('"');
    escaped
}

fn node_policy_version(version: &str) -> String {
    match version.split('.').next() {
        Some(major) if !major.is_empty() => format!("{major}.x"),
        _ => version.to_string(),
    }
}

fn current_platform() -> String {
    let arch = match std::env::consts::ARCH {
        "aarch64" => "arm64",
        other => other,
    };
    format!("{}-{arch}", std::env::consts::OS)
}

fn is_homebrew_tool(tool: &ToolStatus) -> bool {
    path_contains(tool, "/homebrew/")
        || path_contains(tool, "/opt/homebrew/")
        || path_contains(tool, "/usr/local/")
}

fn path_contains(tool: &ToolStatus, pattern: &str) -> bool {
    tool.path
        .as_ref()
        .is_some_and(|path| path.to_string_lossy().contains(pattern))
}

